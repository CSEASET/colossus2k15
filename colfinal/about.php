						<p>“Colossus” the newest jewel embedded into the crown of IEEEASET was the first technical festival organized on January 21-22 by IEEE Student Branch of Amity School of Engineering & Technology, Bijwasan, New Delhi. The two days of extravaganza saw overwhelming participation from IEEE Student Branches of Delhi Section & many other colleges. </p>
						<p>Colossus v2.0, a second installment to our annual technical festival under IEEEASET started with warm introduction to all the events and workshops in store for the two day extravaganza dated 13th - 14th October.</p>
						<p>The fest was inaugurated by our esteemed student branch counselor Mr. Sanjeev Kumar who facilitated the chief guests, our senior director Mr. B.P. Singh, and ASET alumnus Mr.  Karandeep Singh with bouquets. The opening ceremony continued with the lightning of lamp by our chief guests and our president Siddharth Choudhary. After that our guest and branch counselor expressed his ideas on ASET’s journey from Colossus ’12 to Colossus v2.0 familiarizing the participants with the events and wishing them well for a successful and competitive fest.</p>
						<p>Throughout the duration of the technical festival, the college was bustling with a potpourri of technical events for the analytical mind bearing testimony to this proposal. PRYAAS an initiative taken by the students of IEEEASET enhanced in coordination among various neighbouring IEEE branches it encouraged a greater participation in the inter college event and hence increased competitiveness. </p>
						<p>A total of three online & twelve offline events were organized during the festival. Students from different colleges were seen to be enthusiastically registering themselves for various events & workshops. Events judging every other talent like technical writing skills, photography, programming, debating, entrepreneurship skills, etc were organized. The online events namely Pixel: Photography Contest, Word War: Article Writing Competition & Krack & Track: Treasure Hunt were organized for two weeks before the festival. These events were great success & people (students & professionals) from across the world participated in it. </p>
						<p>At COLOSSUSv2.0, one of the premiere events which stole the show was Roborace. It was clearly the most anticipated, talked about and participated event. The second iteration of this event saw an overwhelming participation from about 15 different colleges and 38 inter college teams. The no. of participants increased and so did the level of the race. The track wasn’t easy at all and needed precise judgment of the corners and the hurdles. There was an interesting mix of slow turns and high speed turns with suitable hurdles following the turns to give the controller a taste of toughness. We saw some mind boggling performances by different robots. Another event of the same scale was CodeSence it was the C/C++ programming challenge that allowed the participants to exercise their analytical side while the clock was ticking fast. A team of two cumulated their minds to clear through 2 rounds i.e. MCQ (Prelims) and Code on Compiler (Deciding 2). It was a major event of IEEEASET's tech fest "Colossus" having participations about 175+ teams i,e, 300+ participants.</p>
						<p>The fest witnessed participations from NSIT, NIEC, IIIT Delhi, ASET, MSIT, JMI, Jamia Hamdard, MAIT, HMR, GTBI, DTU, VIS, USIT, PGDAV, GBPEC, and many other colleges. Besides the events COLOSSUS v2.0 also gave the participants the opportunity to learn and interact with the intellectuals by organizing 2 workshops.</p>
						<p>A workshop on Ray Tracing was taken by the APPIN technologies, the 4th largest infrastructure security company in the world. The workshop showcased OpenGL as a tool of multimedia with a language independent feature, the latest technology which is used in designing of automobiles, infrastructure and animations. How can we use it with GCC Compiler. How Physics and Statistical theorems are involved in it. A Case Study, showing an example by running an executable Ray Tracer(Multimedia enable) and the future of this technology. Participants from over 20 colleges attended the workshop taking the participants’ count to over 100.On the second day Students from DTU organized a seminar on Android, making people aware about the basics of Android, and the future scope of this technology which has revolutionalised the world.</p>
						<p>The two day technical fiesta was a fete with a promise, a promise to render a plethora of pleasure and opportunities without compromising on the merry-making that comes along. In a world of unimaginable diversity it stands out like a Mosaic at an exhibition. True to its name, the fest enjoyed colossal success and did bear a really sweet fruit in the minds of every individual. </p>		
